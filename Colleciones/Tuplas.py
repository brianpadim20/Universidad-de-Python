# Definición de tupla
frutas = ("Manzana", "Pera", "Naranja", "Banano")

# Cantidad de elementos 
print(len(frutas))

# Acceder a un elemento en particular
print(frutas[2])

# Acceder a un rango de valores
print(frutas[0:2])# El índice 2 no se incluye

# Recorrer elementos de una tupla
for i in frutas:
    print (i)

